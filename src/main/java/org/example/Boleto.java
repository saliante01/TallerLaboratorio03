package org.example;

public class Boleto {
    private int numero;
    private String letra;

    public Boleto(){
        this.numero=numero;
        this.letra=letra;

    }
    public int getNumero(){return numero;}
    public void setNumero(){this.numero=numero;}

    public String getLetra(){return letra;}
    public void setLetra(String letra){this.letra=letra;}
}
